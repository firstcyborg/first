package org.example;

public class Amine {
    boolean b;
    String string;

    public Amine(boolean b, String string) {
        this.b = b;
        this.string = string;
    }

    public boolean isB() {
        return b;
    }

    public void setB(boolean b) {
        this.b = b;
    }

    public String getString() {
        return string;
    }

    public void setString(String string) {
        this.string = string;
    }
}
